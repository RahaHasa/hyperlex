#!/usr/bin/env node
/**
 * Скрипт для распаковки BZ2 файла из base64
 */

const fs = require('fs');
const path = require('path');
const { execSync } = require('child_process');

// BZ2 файл в бинарном формате (из вложения)
const bz2Data = Buffer.from([
  0x42, 0x5a, 0x68, 0x39, 0x31, 0x41, 0x59, 0x26, 0x53, 0x59, 0x1d, 0x72, 0x34, 0xd5, 0x00, 0x02,
  0xd7, 0xc8, 0x00, 0x40, 0x30, 0x7f, 0xe0, 0x60, 0x06, 0x7b, 0x03, 0xa7, 0xd4, 0xd9, 0x80, 0x7c,
  0x59, 0xf7, 0xb7, 0xdb, 0xef, 0x98, 0xf5, 0xee, 0xed, 0xdd, 0xf0, 0xd5, 0x3f, 0x43, 0x54, 0xd8,
  0xd4, 0x0d, 0x4f, 0x20, 0xc9, 0x49, 0x03, 0x53, 0x19, 0x35, 0x34, 0xd4, 0x0d, 0x53, 0xf6, 0xa0,
  0x4d, 0x14, 0x0d, 0x4f, 0xf5, 0x48, 0xd2, 0x9e, 0x28, 0x09, 0x4d, 0x00, 0x53, 0x50, 0x77, 0xe7,
  0xe9, 0x1a, 0x74, 0x18, 0x3e, 0x65, 0xdd, 0xd4, 0x4c, 0xe9, 0xa1, 0xf0, 0x65, 0xb8, 0x29, 0x3c,
  0x2c, 0x93, 0x71, 0x16, 0xe0, 0x7d, 0xcd, 0xe6, 0x99, 0xb3, 0x57, 0x89, 0x56, 0xc7, 0x27, 0xbb,
  0x54, 0x2d, 0xea, 0xa8, 0xab, 0xb1, 0xf1, 0xbb, 0x57, 0xd8, 0x9b, 0xda, 0xbd, 0xd0, 0xa0, 0xa8
]);

const tempBz2 = path.join(__dirname, 'server', 'data', 'temp_uzb-rus_links.tsv.bz2');
const outputTsv = path.join(__dirname, 'server', 'data', 'uzb-rus_links.tsv');

console.log('📦 Сохраняю BZ2 файл...');
fs.writeFileSync(tempBz2, bz2Data);
console.log(`✓ Файл сохранен: ${tempBz2}`);

console.log('📂 Распаковываю...');
try {
  execSync(`bunzip2 -c "${tempBz2}" > "${outputTsv}"`, { stdio: 'inherit' });
  console.log(`✓ Файл распакован: ${outputTsv}`);
  
  // Показываю первые строки
  const content = fs.readFileSync(outputTsv, 'utf-8');
  const lines = content.split('\n');
  console.log(`\n📊 Файл содержит ${lines.length - 1} строк\n`);
  console.log('Первые 10 строк:');
  lines.slice(0, 10).forEach((line, i) => {
    console.log(`${i}: ${line}`);
  });
  
  // Удаляю временный файл
  fs.unlinkSync(tempBz2);
  console.log('\n✓ Готово к импорту!');
} catch (err) {
  console.error('❌ Ошибка:', err.message);
  process.exit(1);
}
